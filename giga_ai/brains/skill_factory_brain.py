"""
skill_factory_brain.py – The ninth brain. Gigia's skill creation engine.

Responsibilities
----------------
1. PATTERN DISCOVERY → COMPOSITION
   Tracks which skill sequences succeed repeatedly. When the same sequence
   wins N times, it composes them into a new single skill registered in almcp.

2. GAP DETECTION → CODE GENERATION
   Listens for SKILL_GAP_DETECTED events. When PlanningBrain can't find a
   suitable skill, this brain generates Python code for one, validates it,
   and stages it — ready to be used for the current goal without being
   globally visible yet.

   Lifecycle:  draft → staged → active → disabled

   draft   : code generated by LLM, stored in executor SQLite
   staged  : passed strict AST validation + local test run; usable for
             the current goal but NOT in the global almcp catalog
   active  : ran successfully in production with valid output; registered
             in almcp via POST /api/skills/generate; globally discoverable
   disabled: failed any validation, test, or runtime check

3. METADATA IMPROVEMENT
   Reads the full catalog from almcp and improves weak metadata (tags,
   descriptions, best_used_when) using real execution data.

almcp endpoints used
--------------------
  GET   /api/skills/catalog   — read global catalog
  POST  /api/skills/compose   — register composed skill
  PATCH /api/skills/improve   — update metadata on existing skill
  POST  /api/skills/generate  — register a code-generated skill (active)
"""

